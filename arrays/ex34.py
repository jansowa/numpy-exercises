import numpy as np

print("Array of default (float) zeroes:")
print(np.zeros((2, 2)))
print()

print("Array of default (float) ones:")
print(np.ones((2, 2)))
print()

print("Array of int zeroes:")
print(np.zeros((2, 2), dtype=int))
print()

print("Array of int ones:")
print(np.ones((2, 2), dtype=int))
print()
